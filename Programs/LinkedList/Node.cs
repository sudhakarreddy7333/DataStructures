namespace DSAlgorithms.Programs.LinkedList
{
    /**
 * https://leetcode.com/problems/add-two-numbers/
 */

    public class Node
    {
        public int val;
        public Node next;
        public Node(int val = 0, Node next = null)
        {
            this.val = val;
            this.next = next;
        }
    }
}
